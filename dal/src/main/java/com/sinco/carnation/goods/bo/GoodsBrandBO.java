package com.sinco.carnation.goods.bo;


import java.util.List;

import com.sinco.carnation.goods.model.GoodsBrand;
/**
 * 
 * BO 用于返回数据
 *
 */
@SuppressWarnings("serial")
public class GoodsBrandBO extends GoodsBrand{

	private String categoryName;
	
	private String className;
	
	private String storeName;
	
	private List<GoodsBrandBO> brandBoList;
	
	
	public String getStoreName() {
		return storeName;
	}

	public void setStoreName(String storeName) {
		this.storeName = storeName;
	}

	public String getClassName() {
		return className;
	}

	public void setClassName(String className) {
		this.className = className;
	}

	public String getCategoryName() {
		return categoryName;
	}

	public void setCategoryName(String categoryName) {
		this.categoryName = categoryName;
	}

	public List<GoodsBrandBO> getBrandBoList() {
		return brandBoList;
	}

	public void setBrandBoList(List<GoodsBrandBO> brandBoList) {
		this.brandBoList = brandBoList;
	}
	
	
}