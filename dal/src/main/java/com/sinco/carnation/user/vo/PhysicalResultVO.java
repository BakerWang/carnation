package com.sinco.carnation.user.vo;

import com.sinco.carnation.user.model.PhysicalResult;

public class PhysicalResultVO extends PhysicalResult {
	/**
	 * 
	 */
	private static final long serialVersionUID = 4969872805757738736L;
	private String nickname;
	private Integer sex;
	private Integer age;
	private String typeName;
	private Integer resultType;
	private String memo;

	public String getNickname() {
		return nickname;
	}

	public void setNickname(String nickname) {
		this.nickname = nickname;
	}

	public Integer getSex() {
		return sex;
	}

	public void setSex(Integer sex) {
		this.sex = sex;
	}

	public Integer getAge() {
		return age;
	}

	public void setAge(Integer age) {
		this.age = age;
	}

	public String getTypeName() {
		return typeName;
	}

	public void setTypeName(String typeName) {
		this.typeName = typeName;
	}

	public Integer getResultType() {
		return resultType;
	}

	public void setResultType(Integer resultType) {
		this.resultType = resultType;
	}

	public String getMemo() {
		return memo;
	}

	public void setMemo(String memo) {
		this.memo = memo;
	}

}