package com.sinco.carnation.user.bo;

import com.sinco.carnation.user.model.Album;

/**
 * 
 * BO 用于返回数据
 * 
 */
public class AlbumBO extends Album {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	private Long accId;

	private String path;

	private Integer accessorySize;

	private String loginName;

	public Long getAccId() {
		return accId;
	}

	public String getPath() {
		return path;
	}

	public void setAccId(Long accId) {
		this.accId = accId;
	}

	public void setPath(String path) {
		this.path = path;
	}

	public Integer getAccessorySize() {
		return accessorySize;
	}

	public void setAccessorySize(Integer accessorySize) {
		this.accessorySize = accessorySize;
	}

	public String getLoginName() {
		return loginName;
	}

	public void setLoginName(String loginName) {
		this.loginName = loginName;
	}

}