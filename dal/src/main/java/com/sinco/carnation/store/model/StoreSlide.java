package com.sinco.carnation.store.model;

import com.sinco.mybatis.dal.model.BaseModel;
import java.util.Date;

public class StoreSlide extends BaseModel {
	/**
	 * 
	 */
	private static final long serialVersionUID = -5457121600420757574L;

	/**  **/
	private Long id;

	/**  **/
	private Date addTime;

	/**  **/
	private Boolean deleteStatus;

	/** 0为店铺设置默认幻灯，1为店铺装修中设置的模块幻灯 **/
	private Integer slideType;

	/**  **/
	private String url;

	/**  **/
	private String accPath;

	/**  **/
	private Long storeId;

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public Date getAddTime() {
		return addTime;
	}

	public void setAddTime(Date addTime) {
		this.addTime = addTime;
	}

	public Boolean getDeleteStatus() {
		return deleteStatus;
	}

	public void setDeleteStatus(Boolean deleteStatus) {
		this.deleteStatus = deleteStatus;
	}

	public Integer getSlideType() {
		return slideType;
	}

	public void setSlideType(Integer slideType) {
		this.slideType = slideType;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}

	public String getAccPath() {
		return accPath;
	}

	public void setAccPath(String accPath) {
		this.accPath = accPath;
	}

	public Long getStoreId() {
		return storeId;
	}

	public void setStoreId(Long storeId) {
		this.storeId = storeId;
	}
}