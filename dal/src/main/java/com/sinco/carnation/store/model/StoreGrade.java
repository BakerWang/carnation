package com.sinco.carnation.store.model;

import com.sinco.mybatis.dal.model.BaseModel;
import java.util.Date;

public class StoreGrade extends BaseModel {
	/**
	 * serialVersionUID
	 */
	private static final long serialVersionUID = 4585988464335931321L;

	/**  **/
	private Long id;

	/**  **/
	private Date addTime;

	/**  **/
	private Boolean deleteStatus;

	/** 允许卖家添加的子账户个数 **/
	private Integer acountNum;

	/**  **/
	private String addFunciton;

	/** 申请该等级店铺是否需要审核 **/
	private Boolean audit;

	/** 允许发布商品数量 **/
	private Integer goodsCount;

	/** 发布的商品是否需要平台审核，0为需要审核，1为不需要审核 **/
	private Integer goodsAudit;

	/** 等级级别，数据越大级别越高 **/
	private Integer gradeLevel;

	/** 等级名称 **/
	private String gradeName;

	/** 店铺主营限制，0-不限制，1-限制 **/
	private Integer mainLimit;

	/** 收费标准 **/
	private String price;

	/**  **/
	private Integer sequence;

	/** 店铺图片空间大小 **/
	private Float spaceSize;

	/** 是否系统店铺等级，系统店铺等级不可删除 **/
	private Boolean sysGrade;

	/** 该等级店铺允许使用的模板样式，样式列表使用,符号间隔 **/
	private String templates;

	/** 是否可用0元试用功能 0为否 1为是 **/
	private Boolean whetherFree;

	/** 申请说明 **/
	private String content;

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public Date getAddTime() {
		return addTime;
	}

	public void setAddTime(Date addTime) {
		this.addTime = addTime;
	}

	public Boolean getDeleteStatus() {
		return deleteStatus;
	}

	public void setDeleteStatus(Boolean deleteStatus) {
		this.deleteStatus = deleteStatus;
	}

	public Integer getAcountNum() {
		return acountNum;
	}

	public void setAcountNum(Integer acountNum) {
		this.acountNum = acountNum;
	}

	public String getAddFunciton() {
		return addFunciton;
	}

	public void setAddFunciton(String addFunciton) {
		this.addFunciton = addFunciton;
	}

	public Boolean getAudit() {
		return audit;
	}

	public void setAudit(Boolean audit) {
		this.audit = audit;
	}

	public Integer getGoodsCount() {
		return goodsCount;
	}

	public void setGoodsCount(Integer goodsCount) {
		this.goodsCount = goodsCount;
	}

	public Integer getGoodsAudit() {
		return goodsAudit;
	}

	public void setGoodsAudit(Integer goodsAudit) {
		this.goodsAudit = goodsAudit;
	}

	public Integer getGradeLevel() {
		return gradeLevel;
	}

	public void setGradeLevel(Integer gradeLevel) {
		this.gradeLevel = gradeLevel;
	}

	public String getGradeName() {
		return gradeName;
	}

	public void setGradeName(String gradeName) {
		this.gradeName = gradeName;
	}

	public Integer getMainLimit() {
		return mainLimit;
	}

	public void setMainLimit(Integer mainLimit) {
		this.mainLimit = mainLimit;
	}

	public String getPrice() {
		return price;
	}

	public void setPrice(String price) {
		this.price = price;
	}

	public Integer getSequence() {
		return sequence;
	}

	public void setSequence(Integer sequence) {
		this.sequence = sequence;
	}

	public Float getSpaceSize() {
		return spaceSize;
	}

	public void setSpaceSize(Float spaceSize) {
		this.spaceSize = spaceSize;
	}

	public Boolean getSysGrade() {
		return sysGrade;
	}

	public void setSysGrade(Boolean sysGrade) {
		this.sysGrade = sysGrade;
	}

	public String getTemplates() {
		return templates;
	}

	public void setTemplates(String templates) {
		this.templates = templates;
	}

	public Boolean getWhetherFree() {
		return whetherFree;
	}

	public void setWhetherFree(Boolean whetherFree) {
		this.whetherFree = whetherFree;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}
}