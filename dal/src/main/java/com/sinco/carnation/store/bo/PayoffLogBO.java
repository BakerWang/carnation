package com.sinco.carnation.store.bo;

import com.sinco.carnation.store.model.PayoffLog;
import com.sinco.carnation.user.model.Manager;
/**
 * 
 * BO 用于返回数据
 *
 */
import com.sinco.carnation.user.model.Seller;

public class PayoffLogBO extends PayoffLog {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1779568694403876317L;

	private Seller seller;

	private Manager admin;

	private String managerName;

	private String sellerNickname;

	private String storeName;

	public String getStoreName() {
		return storeName;
	}

	public void setStoreName(String storeName) {
		this.storeName = storeName;
	}

	public String getSellerNickname() {
		return sellerNickname;
	}

	public void setSellerNickname(String sellerNickname) {
		this.sellerNickname = sellerNickname;
	}

	public String getManagerName() {
		return managerName;
	}

	public void setManagerName(String managerName) {
		this.managerName = managerName;
	}

	public Manager getAdmin() {
		return admin;
	}

	public void setAdmin(Manager admin) {
		this.admin = admin;
	}

	public Seller getSeller() {
		return seller;
	}

	public void setSeller(Seller seller) {
		this.seller = seller;
	}

}