package com.sinco.carnation.sns.model;

import com.sinco.mybatis.dal.model.BaseModel;
import java.util.Date;

/**
 * 用户与健康任务关系类
 * 
 * @author yuleijia
 */
public class UserHealthtaskRelation extends BaseModel {
	/**
	 * 
	 */
	private static final long serialVersionUID = 1227578411204282688L;

	/**  **/
	private Long id;

	/**  **/
	private Long userId;

	/**  **/
	private Long userHealthtaskId;

	/**  **/
	private Date joinTime;

	/**  **/
	private Long isDeleted;

	/** 坚持天数 **/
	private Long doDays;

	/** 用户完成任务时间 **/
	private String finishTime;

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public Long getUserId() {
		return userId;
	}

	public void setUserId(Long userId) {
		this.userId = userId;
	}

	public Long getUserHealthtaskId() {
		return userHealthtaskId;
	}

	public void setUserHealthtaskId(Long userHealthtaskId) {
		this.userHealthtaskId = userHealthtaskId;
	}

	public Date getJoinTime() {
		return joinTime;
	}

	public void setJoinTime(Date joinTime) {
		this.joinTime = joinTime;
	}

	public Long getIsDeleted() {
		return isDeleted;
	}

	public void setIsDeleted(Long isDeleted) {
		this.isDeleted = isDeleted;
	}

	public Long getDoDays() {
		return doDays;
	}

	public void setDoDays(Long doDays) {
		this.doDays = doDays;
	}

	public String getFinishTime() {
		return finishTime;
	}

	public void setFinishTime(String finishTime) {
		this.finishTime = finishTime;
	}
}