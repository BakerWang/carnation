package com.sinco.carnation.sns.model;

import com.sinco.dal.common.persistence.mybatis.DataModel;

public class ConsultingRepay extends DataModel {
	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	/** 主键 **/
	private Long id;

	/** 咨询id **/
	private Long consultingId;

	/** 回复内容 **/
	private String content;

	/**  **/
	private Boolean isDeleted;

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public Long getConsultingId() {
		return consultingId;
	}

	public void setConsultingId(Long consultingId) {
		this.consultingId = consultingId;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}

	public Boolean getIsDeleted() {
		return isDeleted;
	}

	public void setIsDeleted(Boolean isDeleted) {
		this.isDeleted = isDeleted;
	}
}