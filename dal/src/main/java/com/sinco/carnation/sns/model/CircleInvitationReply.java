package com.sinco.carnation.sns.model;

import com.sinco.mybatis.dal.model.BaseModel;
import java.util.Date;

public class CircleInvitationReply extends BaseModel {
	/**
	 * 
	 */
	private static final long serialVersionUID = -6249860560984926210L;

	/**  **/
	private Long id;

	/**  **/
	private Date addTime;

	/**  **/
	private Integer deleteStatus;

	/** 对应的帖子id **/
	private Long invitationId;

	/** 楼层号 **/
	private Integer levelCount;

	/** 上级回复信息id **/
	private Long parentId;

	/** 回复数量，当作为上级回复信息时 **/
	private Integer replyCount;

	/** 对应的回复人id **/
	private Long userId;

	/** 对应的回复人姓名 **/
	private String userName;

	/** 对应的回复人头像路径“upload/” **/
	private String userPhoto;

	/** 回复信息 **/
	private String content;

	/** 帖子图片 **/
	private String pic;
	/** 是否已添加积分 **/
	private Integer isAddIntegral;

	public String getPic() {
		return pic;
	}

	public void setPic(String pic) {
		this.pic = pic;
	}

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public Date getAddTime() {
		return addTime;
	}

	public void setAddTime(Date addTime) {
		this.addTime = addTime;
	}

	public Integer getDeleteStatus() {
		return deleteStatus;
	}

	public void setDeleteStatus(Integer deleteStatus) {
		this.deleteStatus = deleteStatus;
	}

	public Long getInvitationId() {
		return invitationId;
	}

	public void setInvitationId(Long invitationId) {
		this.invitationId = invitationId;
	}

	public Integer getLevelCount() {
		return levelCount;
	}

	public void setLevelCount(Integer levelCount) {
		this.levelCount = levelCount;
	}

	public Long getParentId() {
		return parentId;
	}

	public void setParentId(Long parentId) {
		this.parentId = parentId;
	}

	public Integer getReplyCount() {
		return replyCount;
	}

	public void setReplyCount(Integer replyCount) {
		this.replyCount = replyCount;
	}

	public Long getUserId() {
		return userId;
	}

	public void setUserId(Long userId) {
		this.userId = userId;
	}

	public String getUserName() {
		return userName;
	}

	public void setUserName(String userName) {
		this.userName = userName;
	}

	public String getUserPhoto() {
		return userPhoto;
	}

	public void setUserPhoto(String userPhoto) {
		this.userPhoto = userPhoto;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}

	public Integer getIsAddIntegral() {
		return isAddIntegral;
	}

	public void setIsAddIntegral(Integer isAddIntegral) {
		this.isAddIntegral = isAddIntegral;
	}
}