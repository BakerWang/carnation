package com.sinco.carnation.sns.model;

import java.util.Date;

import com.sinco.dal.common.persistence.mybatis.DataModel;

public class Consulting extends DataModel {
	/**
	 * 
	 */
	private static final long serialVersionUID = 7491430461941187455L;

	/**  **/
	private Long id;

	/** 专家用户id **/
	private Long expertsUserId;

	/** 提问用户id **/
	private Long userId;

	/** 提问标题 **/
	private String title;

	/** 提问图片 **/
	private String images;

	/** 是否删除：0未删除，1已删除 **/
	private Boolean isDeleted;

	/** 提问内容 **/
	private String content;

	/** 最后回复人id **/
	private Long replyUserId;

	/** 回复数量 **/
	private Integer replyCount;

	private String mark;

	public Long getUserId() {
		return userId;
	}

	public void setUserId(Long userId) {
		this.userId = userId;
	}

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public Long getExpertsUserId() {
		return expertsUserId;
	}

	public void setExpertsUserId(Long expertsUserId) {
		this.expertsUserId = expertsUserId;
	}

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getImages() {
		return images;
	}

	public void setImages(String images) {
		this.images = images;
	}

	public Boolean getIsDeleted() {
		return isDeleted;
	}

	public void setIsDeleted(Boolean isDeleted) {
		this.isDeleted = isDeleted;
	}

	public String getContent() {
		return content;
	}

	public void setContent(String content) {
		this.content = content;
	}

	public String getMark() {
		return mark;
	}

	public void setMark(String mark) {
		this.mark = mark;
	}

	public Long getReplyUserId() {
		return replyUserId;
	}

	public Integer getReplyCount() {
		return replyCount;
	}

	public void setReplyUserId(Long replyUserId) {
		this.replyUserId = replyUserId;
	}

	public void setReplyCount(Integer replyCount) {
		this.replyCount = replyCount;
	}

	public Date getCreateTime() {
		return createTime;
	}

	public void setCreateTime(Date createTime) {
		this.createTime = createTime;
	}

}