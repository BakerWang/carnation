package com.sinco.carnation.sns.vo;

import java.util.List;

import com.sinco.carnation.sns.model.Information;

public class InformationVO extends Information {

	/**
	 * 
	 */
	private static final long serialVersionUID = -6049526878701684570L;

	private List<Long> classIds;

	private List<Integer> verifyStatus;

	private String isVerify;

	public List<Long> getClassIds() {
		return classIds;
	}

	public void setClassIds(List<Long> classIds) {
		this.classIds = classIds;
	}

	public List<Integer> getVerifyStatus() {
		return verifyStatus;
	}

	public void setVerifyStatus(List<Integer> verifyStatus) {
		this.verifyStatus = verifyStatus;
	}

	public String getIsVerify() {
		return isVerify;
	}

	public void setIsVerify(String isVerify) {
		this.isVerify = isVerify;
	}
}