package com.sinco.carnation.jiuye.bo;


/**
 * 酒业订单详情类
 * 
 * @author yuanming
 * 
 */
public class JiuyeOrderDetailsBO {

	// 详情ID
	private Long id;
	// 酒业订单ID
	private Long orderId;
	// 优惠券优惠金额
	private String couponFree;
	// 优惠券编号
	private String couponNo;
	// skuID
	private Long skuIId;
	// sku小图标
	private String skuIcon;
	// sku名称
	private String skuName;
	// sku数量
	private int skuNum;
	// sku价格
	private double skuPrice;
	// sku名称
	private String supplierName;

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public Long getOrderId() {
		return orderId;
	}

	public void setOrderId(Long orderId) {
		this.orderId = orderId;
	}

	public String getCouponFree() {
		return couponFree;
	}

	public void setCouponFree(String couponFree) {
		this.couponFree = couponFree;
	}

	public String getCouponNo() {
		return couponNo;
	}

	public void setCouponNo(String couponNo) {
		this.couponNo = couponNo;
	}

	public Long getSkuIId() {
		return skuIId;
	}

	public void setSkuIId(Long skuIId) {
		this.skuIId = skuIId;
	}

	public String getSkuIcon() {
		return skuIcon;
	}

	public void setSkuIcon(String skuIcon) {
		this.skuIcon = skuIcon;
	}

	public String getSkuName() {
		return skuName;
	}

	public void setSkuName(String skuName) {
		this.skuName = skuName;
	}

	public int getSkuNum() {
		return skuNum;
	}

	public void setSkuNum(int skuNum) {
		this.skuNum = skuNum;
	}

	public double getSkuPrice() {
		return skuPrice;
	}

	public void setSkuPrice(double skuPrice) {
		this.skuPrice = skuPrice;
	}

	public String getSupplierName() {
		return supplierName;
	}

	public void setSupplierName(String supplierName) {
		this.supplierName = supplierName;
	}

}