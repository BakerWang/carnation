package com.sinco.carnation.shop.vo;

public class RefundLogVO {
	private String returnLogUserName;
	private String refundId;
	private String returnServiceId;
	private String startDate, endDate;

	public String getReturnLogUserName() {
		return returnLogUserName;
	}

	public void setReturnLogUserName(String returnLogUserName) {
		this.returnLogUserName = returnLogUserName;
	}

	public String getRefundId() {
		return refundId;
	}

	public void setRefundId(String refundId) {
		this.refundId = refundId;
	}

	public String getReturnServiceId() {
		return returnServiceId;
	}

	public void setReturnServiceId(String returnServiceId) {
		this.returnServiceId = returnServiceId;
	}

	public String getStartDate() {
		return startDate;
	}

	public void setStartDate(String startDate) {
		this.startDate = startDate;
	}

	public String getEndDate() {
		return endDate;
	}

	public void setEndDate(String endDate) {
		this.endDate = endDate;
	}
}
