package com.sinco.carnation.sys.vo;

import java.util.Date;
import java.util.List;

public class ArticleShopVO {

	private String mark;
	private Date gtAddTime;

	private Date ltAddTime;

	private Long classId;

	private List<Long> classIds;

	private String type;

	private Boolean display;

	private Integer count;

	public String getMark() {
		return mark;
	}

	public void setMark(String mark) {
		this.mark = mark;
	}

	public List<Long> getClassIds() {
		return classIds;
	}

	public void setClassIds(List<Long> classIds) {
		this.classIds = classIds;
	}

	public Date getGtAddTime() {
		return gtAddTime;
	}

	public void setGtAddTime(Date gtAddTime) {
		this.gtAddTime = gtAddTime;
	}

	public Date getLtAddTime() {
		return ltAddTime;
	}

	public void setLtAddTime(Date ltAddTime) {
		this.ltAddTime = ltAddTime;
	}

	public Long getClassId() {
		return classId;
	}

	public void setClassId(Long classId) {
		this.classId = classId;
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public Boolean getDisplay() {
		return display;
	}

	public void setDisplay(Boolean display) {
		this.display = display;
	}

	public Integer getCount() {
		return count;
	}

	public void setCount(Integer count) {
		this.count = count;
	}
}