package com.sinco.carnation.sys.bo;

import com.sinco.carnation.sys.model.IntegralReg;

/**
 * 
 * BO 用于返回数据
 * 
 */
public class IntegralRegBO extends IntegralReg {
	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;

	/** 今日完成次数 **/
	private int today_times;
	/** 今日剩余次数 **/
	private int UFtimes;
	/** 今日完成情况 **/
	private int UF;

	public int getToday_times() {
		return today_times;
	}

	public void setToday_times(int today_times) {
		this.today_times = today_times;
	}

	public int getUFtimes() {
		return UFtimes;
	}

	public void setUFtimes(int uFtimes) {
		UFtimes = uFtimes;
	}

	public int getUF() {
		return UF;
	}

	public void setUF(int uF) {
		UF = uF;
	}

}