package com.sinco.carnation.operation.model;

import com.sinco.mybatis.dal.model.BaseModel;
import java.util.Date;

public class Coupon extends BaseModel {
	/**
	 * 
	 */
	private static final long serialVersionUID = 4005608371208004178L;

	/**  **/
	private Long id;

	/**  **/
	private Date addTime;

	/**  **/
	private Boolean deleteStatus;

	/** 优惠券金额 **/
	private Long couponAmount;

	/** 优惠券使用开始时间 **/
	private Date couponBeginTime;

	/** 优惠券发行数量 **/
	private Integer couponCount;

	/** 优惠券使用结束时间 **/
	private Date couponEndTime;

	/** 优惠券名称 **/
	private String couponName;

	/** 优惠券使用的订单金额，订单满足该金额时才可以使用该优惠券 **/
	private Long couponOrderAmount;

	/** 优惠券类型，0为平台优惠券，抵消自营商品订单金额，1为商家优惠券，抵消订单中该商家商品部分金额 **/
	private Integer couponType;

	/** 优惠券图片 **/
	private String couponAcc;

	/**  **/
	private Long storeId;

	/** 春节活动id **/
	private Long newYearId;

	public Long getNewYearId() {
		return newYearId;
	}

	public void setNewYearId(Long newYearId) {
		this.newYearId = newYearId;
	}

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public Date getAddTime() {
		return addTime;
	}

	public void setAddTime(Date addTime) {
		this.addTime = addTime;
	}

	public Boolean getDeleteStatus() {
		return deleteStatus;
	}

	public void setDeleteStatus(Boolean deleteStatus) {
		this.deleteStatus = deleteStatus;
	}

	public Long getCouponAmount() {
		return couponAmount;
	}

	public void setCouponAmount(Long couponAmount) {
		this.couponAmount = couponAmount;
	}

	public Date getCouponBeginTime() {
		return couponBeginTime;
	}

	public void setCouponBeginTime(Date couponBeginTime) {
		this.couponBeginTime = couponBeginTime;
	}

	public Integer getCouponCount() {
		return couponCount;
	}

	public void setCouponCount(Integer couponCount) {
		this.couponCount = couponCount;
	}

	public Date getCouponEndTime() {
		return couponEndTime;
	}

	public void setCouponEndTime(Date couponEndTime) {
		this.couponEndTime = couponEndTime;
	}

	public String getCouponName() {
		return couponName;
	}

	public void setCouponName(String couponName) {
		this.couponName = couponName;
	}

	public Long getCouponOrderAmount() {
		return couponOrderAmount;
	}

	public void setCouponOrderAmount(Long couponOrderAmount) {
		this.couponOrderAmount = couponOrderAmount;
	}

	public Integer getCouponType() {
		return couponType;
	}

	public void setCouponType(Integer couponType) {
		this.couponType = couponType;
	}

	public String getCouponAcc() {
		return couponAcc;
	}

	public void setCouponAcc(String couponAcc) {
		this.couponAcc = couponAcc;
	}

	public Long getStoreId() {
		return storeId;
	}

	public void setStoreId(Long storeId) {
		this.storeId = storeId;
	}
}