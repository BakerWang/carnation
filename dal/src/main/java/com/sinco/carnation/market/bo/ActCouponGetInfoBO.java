package com.sinco.carnation.market.bo;

import com.sinco.carnation.market.model.ActCouponGetInfo;

/**
 * 
 * BO 用于返回数据
 * 
 */
public class ActCouponGetInfoBO extends ActCouponGetInfo {

	/**
	 * 
	 */
	private static final long serialVersionUID = 7788130993013165075L;

	private String mobile;

	private String nickName;

	private String schemeName;

	private String name;

	public String getMobile() {
		return mobile;
	}

	public void setMobile(String mobile) {
		this.mobile = mobile;
	}

	public String getNickName() {
		return nickName;
	}

	public void setNickName(String nickName) {
		this.nickName = nickName;
	}

	public String getSchemeName() {
		return schemeName;
	}

	public void setSchemeName(String schemeName) {
		this.schemeName = schemeName;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}