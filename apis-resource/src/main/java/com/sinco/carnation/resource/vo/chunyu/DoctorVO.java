package com.sinco.carnation.resource.vo.chunyu;

import com.sinco.api.annotation.Param;
import com.sinco.api.request.BasicParamVO;

public class DoctorVO extends BasicParamVO {
	@Param(desc = "医生ID")
	private String id;
	@Param(desc = "医生名称")
	private String name;
	@Param(desc = "医生头像")
	private String image;
	@Param(desc = "医生职称")
	private String title;
	@Param(desc = "医生职称")
	private String level_title;
	@Param(desc = "科室名称")
	private String clinic;
	@Param(desc = "科室号")
	private String clinic_no;
	@Param(desc = "医院名字")
	private String hospital;
	@Param(desc = "医院级别")
	private String hospital_grade;
	@Param(desc = "擅长领域")
	private String good_at;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getImage() {
		return image;
	}
	public void setImage(String image) {
		this.image = image;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getLevel_title() {
		return level_title;
	}
	public void setLevel_title(String level_title) {
		this.level_title = level_title;
	}
	public String getClinic() {
		return clinic;
	}
	public void setClinic(String clinic) {
		this.clinic = clinic;
	}
	public String getClinic_no() {
		return clinic_no;
	}
	public void setClinic_no(String clinic_no) {
		this.clinic_no = clinic_no;
	}
	public String getHospital() {
		return hospital;
	}
	public void setHospital(String hospital) {
		this.hospital = hospital;
	}
	public String getHospital_grade() {
		return hospital_grade;
	}
	public void setHospital_grade(String hospital_grade) {
		this.hospital_grade = hospital_grade;
	}
	public String getGood_at() {
		return good_at;
	}
	public void setGood_at(String good_at) {
		this.good_at = good_at;
	}
}
