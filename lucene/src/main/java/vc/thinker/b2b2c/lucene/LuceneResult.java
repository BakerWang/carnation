public class LuceneResult {
	@SuppressWarnings("rawtypes")
	private List vo_list = new ArrayList();
	private int pages;// 总页数
	private int rows;// 总记录数
	private int currentPage;// 当前页码
	private int pageSize;// 每页大小
	private Set<String> goodsClassList = new ArraySet<String>();

	public int getPages() {
		return pages;
	}

	public void setPages(int pages) {
		this.pages = pages;
	}

	public int getRows() {
		return rows;
	}

	public void setRows(int rows) {
		this.rows = rows;
	}

	public int getCurrentPage() {
		return currentPage;
	}

	public void setCurrentPage(int currentPage) {
		this.currentPage = currentPage;
	}

	public int getPageSize() {
		return pageSize;
	}

	public void setPageSize(int pageSize) {
		this.pageSize = pageSize;
	}

	@SuppressWarnings("rawtypes")
	public List getVo_list() {
		return vo_list;
	}

	@SuppressWarnings("rawtypes")
	public void setVo_list(List vo_list) {
		this.vo_list = vo_list;
	}

	public Set<String> getGoodsClassList() {
		return goodsClassList;
	}

	public void setGoodsClassList(Set<String> goodsClassList) {
		this.goodsClassList = goodsClassList;
	}

}
