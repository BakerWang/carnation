package vc.thinker.b2b2c.tools.exception;

import org.springframework.security.authentication.AccountStatusException;

public class AccountException extends AccountStatusException {

	private static final long serialVersionUID = -4047338277618230671L;

	public AccountException(String msg) {
		super(msg);
	}

	/**
	 * Constructs a <code>LockedException</code> with the specified message and root cause.
	 * 
	 * @param msg
	 *            the detail message.
	 * @param t
	 *            root cause
	 */
	public AccountException(String msg, Throwable t) {
		super(msg, t);
	}

	@Deprecated
	public AccountException(String msg, Object extraInformation) {
		super(msg, extraInformation);
	}

}
