package com.sinco.carnation.elong.schedule;

import java.io.Serializable;
import java.util.Date;

public class Schedule implements Serializable {
	private static final long serialVersionUID = 1L;
	private String schedule_id;
	private String schedule_tag;
	private Date schedule_time;
	
	protected Schedule(){}
	public Schedule(String schedule_id, String schedule_tag, Date schedule_time){
		this.schedule_id = schedule_id;
		this.schedule_time = schedule_time;
		this.schedule_tag = schedule_tag;
	}
	public String getSchedule_id() {
		return schedule_id;
	}
	public void setSchedule_id(String schedule_id) {
		this.schedule_id = schedule_id;
	}
	public Date getSchedule_time() {
		return schedule_time;
	}
	public void setSchedule_time(Date schedule_time) {
		this.schedule_time = schedule_time;
	}
	public String getSchedule_tag() {
		return schedule_tag;
	}
	public void setSchedule_tag(String schedule_tag) {
		this.schedule_tag = schedule_tag;
	}
	
}
